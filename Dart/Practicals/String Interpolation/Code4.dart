// String Concatenation
// (With using string interpolation)

void main() {
  int empId = 20;
  print("My employee id is $empId"); //My employee id is 20

  String firstName = "Incubators ";
  print("My name is $firstName"); // My name is Incubators

  String lastName = "Systems";
  print("My last name is $lastName"); // My last name is Systems

// Concat String

  print("First Name: $firstName, Last Name: $lastName"); // First Name: Incubators, Last Name: Systems
}
